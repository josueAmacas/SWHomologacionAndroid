package com.example.sistemahomologacion.modelo;

public class Docente {
    private int idD;
    private int cedulaD;
    private String apellidoD;
    private String nombreD;
    private String emailD;
    private String tituloD;
    private String carreraD;

    public Docente() {
    }

    public int getIdD() {
        return idD;
    }

    public void setIdD(int idD) {
        this.idD = idD;
    }

    public int getCedulaD() {
        return cedulaD;
    }

    public void setCedulaD(int cedulaD) {
        this.cedulaD = cedulaD;
    }

    public String getApellidoD() {
        return apellidoD;
    }

    public void setApellidoD(String apellidoD) {
        this.apellidoD = apellidoD;
    }

    public String getNombreD() {
        return nombreD;
    }

    public void setNombreD(String nombreD) {
        this.nombreD = nombreD;
    }

    public String getEmailD() {
        return emailD;
    }

    public void setEmailD(String emailD) {
        this.emailD = emailD;
    }

    public String getTituloD() {
        return tituloD;
    }

    public void setTituloD(String tituloD) {
        this.tituloD = tituloD;
    }

    public String getCarreraD() {
        return carreraD;
    }

    public void setCarreraD(String carreraD) {
        this.carreraD = carreraD;
    }
}
